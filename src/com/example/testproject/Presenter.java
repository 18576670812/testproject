package com.example.testproject;

import android.app.Notification;
import android.app.NotificationManager;
import android.content.BroadcastReceiver;
import android.content.Context;
import android.content.Intent;
import android.content.IntentFilter;
import android.hardware.SensorManager;
import android.view.OrientationEventListener;

public class Presenter {
	static final String TAG = "Presenter";
	static Presenter sInstance = null;
	private Context mContext = null;
	private int sPreOrientation = 0;
	
	private OrientationEventListener mOrientationEventListener = null;
	private BroadcastReceiver mBroadcastReceiver = null;
	
	public final static String ACTION_START_FLASH_LED = "com.hb.START_FLASH_LED";
	public final static String ACTION_STOP_FLASH_LED = "com.hb.STOP_FLASH_LED";
	private final static int NOTIFICATION_ID_LED = 0x10;
	
	private Presenter(Context context) {
		android.util.Log.d(TAG, "Constructor Presenter.");
		mContext = context;
		mOrientationEventListener = 
				new OrientationEventListener(mContext, SensorManager.SENSOR_DELAY_NORMAL) {

					@Override
					public void onOrientationChanged(int arg0) {
						// TODO Auto-generated method stub
						int orientation = arg0;
						android.util.Log.d(TAG, "current orientation: " + sPreOrientation);
						android.util.Log.d(TAG, "new orientation: " + orientation);
						sPreOrientation = orientation;
					}
		};
		
		mBroadcastReceiver = new BroadcastReceiver() {

			@Override
			public void onReceive(Context arg0, Intent arg1) {
				// TODO Auto-generated method stub
				android.util.Log.d(TAG, "onReceive, Action: " + arg1.getAction());
				
				Context context = arg0;
				Intent intent = arg1;
				
				NotificationManager mNotificationManager = 
			    		(NotificationManager) mContext.getSystemService(Context.NOTIFICATION_SERVICE);
				
				if(intent.getAction() == ACTION_START_FLASH_LED) {
					Notification.Builder builder = new Notification.Builder(context);
					builder.setContentText("this notification is used to start led flash");
					builder.setContentTitle("flash LED");
					builder.setSmallIcon(R.drawable.stat_sys_phone_call_forward);
					builder.setLights(0xff00ff00, 1000, 500);
					builder.setVibrate(new long[] {0, 50, 400, 50});
					builder.setAutoCancel(true);
					builder.setShowWhen(true);
					Notification notification = builder.build();
					notification.flags = Notification.FLAG_SHOW_LIGHTS;
					
					android.util.Log.d(TAG, "notify ACTION_START_FLASH_LED");
					mNotificationManager.notify(NOTIFICATION_ID_LED, notification);
				} else if(intent.getAction() == ACTION_STOP_FLASH_LED) {
					android.util.Log.d(TAG, "cancel ACTION_STOP_FLASH_LED");
					mNotificationManager.cancel(NOTIFICATION_ID_LED);
				}
			}
		};
		
		IntentFilter intentFilter = new IntentFilter();
		intentFilter.addAction(ACTION_START_FLASH_LED);
		intentFilter.addAction(ACTION_STOP_FLASH_LED);
		mContext.registerReceiver(mBroadcastReceiver, intentFilter);
	}
	
	public static Presenter getInstance(Context context) {
		if(sInstance == null) {
			sInstance = new Presenter(context);
		}
		return sInstance;
	}
	
	public void enable() {
		android.util.Log.d(TAG, "enable Accelerometer sensor");
		mOrientationEventListener.enable();
	}
	
	public void disable() {
		android.util.Log.d(TAG, "disable Accelerometer sensor");
		mOrientationEventListener.disable();
	}
}